
public class laeufer{

	int startnummer;
	String name;
	int alter;
	int gewicht;
	
	
	public laeufer(int startnummer, String name, int alter, int gewicht)
	{
		this.startnummer= startnummer;
		this.name = name;
		this.alter = alter;
		this.gewicht = gewicht;
	}
	
	public String getName()
	{
		return name;
	}
	
	
}
